# A função capitalize() recebe uma string e formata-a de modo que a primeira letra fique maiúscula e todas as demais, minúsculas. Seu uso segue o mesmo formato das outras funções já vistas.

print(str.capitalize('AeIoU'))

print('aEiOu'.capitalize())

# Na primeira linha, a chamada foi realizada a partir do nome da classe (str); na segunda, diretamente a partir de um objeto dessa classe, no caso, uma string. Os resultados são idênticos.