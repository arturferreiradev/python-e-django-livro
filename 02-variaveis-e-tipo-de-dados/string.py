# Strings a cadeia de caracteres, o python diferente de outras linguagens é zero-based, ou seja, por definição o índice é 0.

saudacao = "Hello World!"

print(saudacao)

print(saudacao[0])

print(saudacao[7])

# Substring é um pedaço de uma outra string
print(saudacao[0:5])

# Concatenação de strings
print("Brasil"+"eiro")
