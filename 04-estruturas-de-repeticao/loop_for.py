# Loops for podem ser usados para iterar sobre coleções (como listas, tuplas etc.), strings e vários outros tipos de dados um número fixo de vezes, executando uma suíte repetidas vezes a cada passada.

# Iterando o for sobre uma coleção(lista, tupla etc.)

alunos = ['Alice','Bob', 'Carl', 'Daniele']

for aluno_atual in alunos:
    print('O/A aluno(a) atualmente examinado é %s' % aluno_atual)
